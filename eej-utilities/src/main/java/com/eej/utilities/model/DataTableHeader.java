package com.eej.utilities.model;

import java.util.List;

public class DataTableHeader {

    String columnName;

    boolean searchable;

    boolean columnSearch;

    boolean sortable;

    String defaultSort;

    String width;

    String dataType;

    boolean displayColumn;

    String defaultClass;

    String defaultContent;

    List<String> valoresFiltro;

    public String getColumnName() {
        return columnName;
    }

    public void setColumnName(String columnName) {
        this.columnName = columnName;
    }

    public boolean isSearchable() {
        return searchable;
    }

    public void setSearchable(boolean searchable) {
        this.searchable = searchable;
    }

    public boolean isColumnSearch() {
        return columnSearch;
    }

    public void setColumnSearch(boolean columnSearch) {
        this.columnSearch = columnSearch;
    }

    public boolean isSortable() {
        return sortable;
    }

    public void setSortable(boolean sortable) {
        this.sortable = sortable;
    }

    public String getDefaultSort() {
        return defaultSort;
    }

    public void setDefaultSort(String defaultSort) {
        this.defaultSort = defaultSort;
    }

    public String getWidth() {
        return width;
    }

    public void setWidth(String width) {
        this.width = width;
    }

    public String getDataType() {
        return dataType;
    }

    public void setDataType(String dataType) {
        this.dataType = dataType;
    }

    public boolean isDisplayColumn() {
        return displayColumn;
    }

    public void setDisplayColumn(boolean displayColumn) {
        this.displayColumn = displayColumn;
    }

    public String getDefaultClass() {
        return defaultClass;
    }

    public void setDefaultClass(String defaultClass) {
        this.defaultClass = defaultClass;
    }

    public String getDefaultContent() {
        return defaultContent;
    }

    public void setDefaultContent(String defaultContent) {
        this.defaultContent = defaultContent;
    }

    public List<String> getValoresFiltro() {
        return valoresFiltro;
    }

    public void setValoresFiltro(List<String> valoresFiltro) {
        this.valoresFiltro = valoresFiltro;
    }
}
